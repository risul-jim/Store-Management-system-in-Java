package Management;

public class InputData {
	public double Ti,Tinv,Es,Eb,Sr;

	public double getTi() {
		return Ti;
	}

	public void setTi(double ti) {
		Ti = ti;
	}

	public double getTinv() {
		return Tinv;
	}

	public void setTinv(double tinv) {
		Tinv = tinv;
	}

	public double getEs() {
		return Es;
	}

	public void setEs(double es) {
		Es = es;
	}

	public double getEb() {
		return Eb;
	}

	public void setEb(double eb) {
		Eb = eb;
	}

	public double getSr() {
		return Sr;
	}

	public void setSr(double sr) {
		Sr = sr;
	}
	

	
}
